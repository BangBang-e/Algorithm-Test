//TODO 2개의 행렬 arr1과 arr2를 입력받아, 행렬 덧셈의 결과를 반환하는 함수, solution을 완성해주세요.

//! <제한 사항>
//! 행렬 arr1, arr2의 행과 열의 길이는 500을 넘지 않습니다.

//* <입출력 예>
//*     arr1=>	        arr2=>	        return=>
//*     [[1,2],[2,3]]	[[3,4],[5,6]]	[[4,6],[7,9]]
//*     [[1],[2]]	    [[3],[4]]	    [[4],[6]]

//::::

function solution(arr1, arr2) {
    return arr1.map((el1, index1) => {
        return el1.map((el2, index2) => el2 + arr2[index1][index2])
    })
}
console.log(solution([[1, 2], [2, 3]], [[3, 4], [5, 6]])); // [[4,6],[7,9]]
